# Pneumonia Prediction Page
if selected == 'Pneumonia Prediction':
    st.title("Pneumonia Detection")
    st.write("Upload a chest X-ray image to predict if it shows signs of pneumonia.")

    # File uploader for the X-ray image
    uploaded_file = st.file_uploader("Choose a chest X-ray image", type=["jpg", "jpeg", "png"])

    # Load the pre-trained pneumonia model
    @st.cache_resource
    def load_pneumonia_model():
        from keras.models import load_model
        return load_model("models/my_model.h5")

    model = load_pneumonia_model()

    # Function to preprocess the image
    def preprocess_image(image, target_size=(150, 150)):
        from PIL import Image
        import numpy as np

        # Resize the image
        image = image.resize(target_size)

        # Convert grayscale to RGB if necessary
        img_array = np.array(image)
        if img_array.ndim == 2:  # Check if the image is grayscale
            img_array = np.stack([img_array] * 3, axis=-1)

        # Normalize pixel values and expand dimensions
        img_array = img_array / 255.0  # Scale to [0, 1]
        return np.expand_dims(img_array, axis=0)  # Add batch dimension

    # When an image is uploaded
    if uploaded_file is not None:
        from PIL import Image
        image = Image.open(uploaded_file)
        st.image(image, caption="Uploaded Chest X-ray", use_column_width=True)

        # Predict button
        if st.button("Predict"):
            try:
                preprocessed_img = preprocess_image(image)
                prediction = model.predict(preprocessed_img)[0][0]  # Assuming binary classification

                if prediction > 0.5:
                    st.error(f"Pneumonia Detected with {prediction * 100:.2f}% confidence.")
                else:
                    st.success(f"No Pneumonia Detected with {(1 - prediction) * 100:.2f}% confidence.")
            except Exception as e:
                st.error(f"An error occurred: {e}")



